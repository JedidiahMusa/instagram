

heart = document.querySelector('like-js');
red_heart = document.querySelector('like1-js');

document.addEventListener('click', function(){
  if (heart.style.display = 'flex') {
    heart.style.display = 'none';
    if(red_heart.style.display = 'none'){
      red_heart.style.display = 'flex';
    }else if(red_heart.style.display = 'flex'){
      red_heart.style.display = 'none';
  } 
  } 
})
